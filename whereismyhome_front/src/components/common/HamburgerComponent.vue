    </div>
  </div>
</template>

<script>
export default {
  props: ["checkMobile"],
  data() {
    return {
      toggleState: this.checkMobile,
    };
  },
  methods: {
    hanburgerToggle() {
      this.$refs.hamburger.classList.toggle("open");
      this.$emit("toggle");
    },
  },
  watch: {
    checkMobile() {
      if (!this.checkMobile) {
        this.$refs.hamburger.classList.remove("open");
      }
    },
  },
};
</script>

